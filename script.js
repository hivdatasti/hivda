let currentSet = [];
let currentIndex = 0;

function startGame(setName) {
    currentSet = wordSets[setName];
    currentIndex = 0;
    displayCard();
}

function displayCard() {
    const container = document.getElementById('card-container');
    container.innerHTML = '';

    if (currentIndex < currentSet.length) {
        const cardData = currentSet[currentIndex];
        const card = document.createElement('div');
        card.className = 'card';
        
        const cardInner = document.createElement('div');
        cardInner.className = 'card-inner';
        
        const cardFront = document.createElement('div');
        cardFront.className = 'card-front';
        cardFront.textContent = cardData.front;
        
        const cardBack = document.createElement('div');
        cardBack.className = 'card-back';
        cardBack.textContent = cardData.back;
        
        cardInner.appendChild(cardFront);
        cardInner.appendChild(cardBack);
        card.appendChild(cardInner);
        
        card.addEventListener('click', () => {
            card.classList.toggle('flipped');
        });
        
        container.appendChild(card);
    } else {
        container.innerHTML = '<p>Bu setin tüm kartlarını görüntülediniz!</p>';
    }
}

function nextCard() {
    if (currentIndex < currentSet.length - 1) {
        currentIndex++;
        displayCard();
    } else {
        alert('Son kartı görüntülediniz. Başka bir set seçebilirsiniz.');
    }
}